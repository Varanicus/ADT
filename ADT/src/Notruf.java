
public class Notruf {
	public int number;
	public String beschreibung;
	public int stunde;
	public int minute;
	public int sekunde;
	public int prio;

	public Notruf(int number, String beschreibung,int stunde, int minute, int sekunde, int prio) {
		this.number=number;
		this.beschreibung=beschreibung;
		this.stunde=stunde;
		this.minute=minute;
		this.sekunde=sekunde;
		this.prio=prio;
	}
}
